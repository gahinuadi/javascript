var sweety = "Sweety";
var cutie = "Cutie";

console.log("Before Swap:");
console.log("Sweety->",sweety);
console.log("Cutie->",cutie);

var temp = sweety;
sweety = cutie;
cutie = temp;

console.log("After swap:");
console.log("Sweety->",sweety);
console.log("Cutie->",cutie);


